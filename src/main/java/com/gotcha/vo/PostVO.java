package com.gotcha.vo;

public class PostVO {
	
	String postId;
	String postTitle;
	String postType;
	String postSubType;
	String postContent;
	String postImage;
	
	int postYear;
	int postMonth;
	int postDay;

	int postStarred;
	int postDetailed;
	int postPinned;
	
	public int getPostPinned() {
		return postPinned;
	}
	public void setPostPinned(int postPinned) {
		this.postPinned = postPinned;
	}
	public int getPostStarred() {
		return postStarred;
	}
	public void setPostStarred(int postStarred) {
		this.postStarred = postStarred;
	}
	public int getPostDetailed() {
		return postDetailed;
	}
	public void setPostDetailed(int postDetailed) {
		this.postDetailed = postDetailed;
	}
	public String getPostSubType() {
		return postSubType;
	}
	public void setPostSubType(String postSubType) {
		this.postSubType = postSubType;
	}
	public String getPostId() {
		return postId;
	}
	public void setPostId(String postId) {
		this.postId = postId;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getPostType() {
		return postType;
	}
	public void setPostType(String postType) {
		this.postType = postType;
	}
	public String getPostContent() {
		return postContent;
	}
	public void setPostContent(String postContent) {
		this.postContent = postContent;
	}
	public String getPostImage() {
		return postImage;
	}
	public void setPostImage(String postImage) {
		this.postImage = postImage;
	}
	public int getPostYear() {
		return postYear;
	}
	public void setPostYear(int postYear) {
		this.postYear = postYear;
	}
	public int getPostMonth() {
		return postMonth;
	}
	public void setPostMonth(int postMonth) {
		this.postMonth = postMonth;
	}
	public int getPostDay() {
		return postDay;
	}
	public void setPostDay(int postDay) {
		this.postDay = postDay;
	}
	
	

}
